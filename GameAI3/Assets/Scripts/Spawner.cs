using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Spawner : MonoBehaviour
{
    private float spawnTimer = 1f;
    private int maxDwarves = 50;
    private int dwarvesSpawned = 0;
    public Transform goal;

    public GameObject dwarfPrefab;

    // Update is called once per frame
    void Update(){
        if(spawnTimer <= 0 && dwarvesSpawned < maxDwarves){
            SpawnDwarf();
            dwarvesSpawned++;
            spawnTimer = 1f;
        }
        spawnTimer -= Time.deltaTime;
    }

    private void SpawnDwarf(){
        GameObject dwarf = Instantiate(dwarfPrefab, new Vector3(transform.position.x, transform.position.y, -2), Quaternion.identity);
        dwarf.GetComponent<DwarfController>().goal = goal;
    }
}
